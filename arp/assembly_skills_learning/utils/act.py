def compute_kld_loss(mu_hat, log_sigma_x2_hat):
    mean_kld = (
        (-0.5 * (1 + log_sigma_x2_hat - mu_hat.pow(2) - (log_sigma_x2_hat).exp())).sum(-1).mean()
    )
    return mean_kld

